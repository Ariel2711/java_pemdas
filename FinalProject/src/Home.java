    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(jTextField1.getText().isEmpty() == false){
            try{
                DecimalFormat dFormat = new DecimalFormat("0.##");
                double inputValue = Double.parseDouble(jTextField1.getText());
                double result = 0.0;
                boolean valid = true;
                String selectedValue1 = (String) jComboBox1.getSelectedItem();
                String selectedValue2 = (String) jComboBox2.getSelectedItem();
                if(selectedValue1.equals("Celcius") && selectedValue2.equals("Reamur"))
                    result = celciusToReamur(inputValue);
                else if(selectedValue1.equals("Celcius") && selectedValue2.equals("Kelvin"))
                    result = celciusToKelvin(inputValue);
                else if(selectedValue1.equals("Celcius") && selectedValue2.equals("Fahrenheit"))
                    result = celciusToFahrenheit(inputValue);
                else if(selectedValue1.equals("Kelvin") && selectedValue2.equals("Celcius"))
                    result = kelvinToCelcius(inputValue);
                else if(selectedValue1.equals("Kelvin") && selectedValue2.equals("Reamur"))
                    result = kelvinToReamur(inputValue);
                else if(selectedValue1.equals("Kelvin") && selectedValue2.equals("Fahrenheit"))
                    result = kelvinToFahrenheit(inputValue);
                else if(selectedValue1.equals("Reamur") && selectedValue2.equals("Celcius"))
                    result = reamurToCelcius(inputValue);
                else if(selectedValue1.equals("Reamur") && selectedValue2.equals("Kelvin"))
                    result = reamurToKelvin(inputValue);
                else if(selectedValue1.equals("Reamur") && selectedValue2.equals("Fahrenheit"))
                    result = reamurToFahrenheit(inputValue);
                else if(selectedValue1.equals("Fahrenheit") && selectedValue2.equals("Celcius"))
                    result = fahrenheitToCelcius(inputValue);
                else if(selectedValue1.equals("Fahrenheit") && selectedValue2.equals("Kelvin"))
                    result = fahrenheitToKelvin(inputValue);
                else if(selectedValue1.equals("Fahrenheit") && selectedValue2.equals("Reamur"))
                    result = fahrenheitToReamur(inputValue);
                else{
                    JOptionPane.showMessageDialog(null, "Temperature units must be difference!", "Same Temperature Units", JOptionPane.INFORMATION_MESSAGE);
                    valid = false;
                }

                if(valid == true){
                    String formatted = dFormat.format(result);
                    jTextField2.setText(formatted);
                }
            }catch (NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Input must be number!", "Wrong Format", JOptionPane.INFORMATION_MESSAGE);
                jTextField1.setText("");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Input the number first!", "Empty Input", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1MouseClicked

