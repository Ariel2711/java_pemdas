    public static double celciusToReamur(double celcius) {
        double reamur = celcius * 0.8;
        return reamur;
    }

    public static double celciusToKelvin(double celcius) {
        double kelvin = celcius + 273.15;
        return kelvin;
    }

    public static double celciusToFahrenheit(double celcius) {
        double fahrenheit = (celcius * 9 / 5) + 32;
        return fahrenheit;
    }

    public static double reamurToCelcius(double reamur) {
        double celcius = reamur * 1.25;
        return celcius;
    }

    public static double reamurToKelvin(double reamur) {
        double kelvin = (reamur * 1.25) + 273.15;
        return kelvin;
    }

    public static double reamurToFahrenheit(double reamur) {
        double fahrenheit = (reamur * 2.25) + 32;
        return fahrenheit;
    }

    public static double kelvinToReamur(double kelvin) {
        double reamur = (kelvin - 273.15) * 0.8;
        return reamur;
    }

    public static double kelvinToCelcius(double kelvin) {
        double celcius = kelvin - 273.15;
        return celcius;
    }

    public static double kelvinToFahrenheit(double kelvin) {
        double fahrenheit = (kelvin - 273.15) * 9 / 5 + 32;
        return fahrenheit;
    }

    public static double fahrenheitToReamur(double fahrenheit) {
        double reamur = (fahrenheit - 32) * 4 / 9;
        return reamur;
    }

    public static double fahrenheitToKelvin(double fahrenheit) {
        double kelvin = (fahrenheit - 32) * 5 / 9 + 273.15;
        return kelvin;
    }

    public static double fahrenheitToCelcius(double fahrenheit) {
        double celcius = (fahrenheit - 32) * 5 / 9;
        return celcius;
    }

